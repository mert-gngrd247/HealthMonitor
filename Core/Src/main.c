/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_DMA_Init();
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */
	// Calibrate the ADC for better accuracy
	HAL_ADCEx_Calibration_Start(&hadc1);

	// Start the ADC in DMA mode
	// It will fill 'adc_buffer' and loop back to start automatically
	HAL_ADC_Start_DMA(&hadc1, (uint32_t*) adc_buffer, 2);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
	char msg[64];

	// 1. Define the Factory Calibration Addresses (From Datasheet)
#define TS_CAL1_ADDR ((uint16_t*) ((uint32_t)0x1FFF75A8)) // Value at 30 degrees C
#define TS_CAL2_ADDR ((uint16_t*) ((uint32_t)0x1FFF75CA)) // Value at 130 degrees C
#ifndef VREFINT_CAL_ADDR
    #define VREFINT_CAL_ADDR   ((uint16_t*) ((uint32_t)0x1FFF75AA)) // 3.0V
#endif

	while (1) {
		// --- Step 1: Calculate Actual System Voltage (VDDA) ---
		// We use the Internal Reference (Vref) to find out the real supply voltage
		uint16_t vref_cal = *VREFINT_CAL_ADDR;
		uint16_t vref_data = adc_buffer[1];

		// Formula: VDDA = 3.0V * (VREF_CAL / VREF_DATA)
		// Result in millivolts (e.g., 3300 mV)
		uint32_t vdda_mv = (3000 * vref_cal) / vref_data;

		// --- Step 2: Calculate Temperature ---
		// We use the slope formula with the calibration points
		int32_t ts_cal1 = (int32_t) *TS_CAL1_ADDR;
		int32_t ts_cal2 = (int32_t) *TS_CAL2_ADDR;

		// Normalize the raw reading to 3.0V reference
		// (Raw * Actual_Voltage) / 3000mV
		int32_t ts_data_raw = (int32_t) adc_buffer[0];
		int32_t ts_data_corrected = (ts_data_raw * vdda_mv) / 3000;
		// Standard Linear Interpolation Formula for STM32
		int32_t temperature_c = ((110 * (ts_data_corrected - ts_cal1))
				/ (ts_cal2 - ts_cal1)) + 30;

		// --- Step 3: Print Results ---
		// CSV Format (Voltage,Temperature)
		sprintf(msg, "%lu,%ld\r\n", vdda_mv, temperature_c);
		//sprintf(msg, "Volt: %lu mV | Temp: %ld C\r\n", vdda_mv, temperature_c);
		HAL_UART_Transmit(&huart2, (uint8_t*) msg, strlen(msg), 100);

		HAL_Delay(1000);

		// --- Step 4: System Protection (The "Thermostat") ---
		// If we touch the chip and it warms up, trigger the alarm.
		if (temperature_c > 31) {
			// HOT! Turn on LED (PC6 for Nucleo-G031K8)
			HAL_GPIO_WritePin(GPIOC, GPIO_PIN_6, GPIO_PIN_SET);
			strcat(msg, " [ALARM: OVERHEATING]"); // Append warning to UART log
		} else {
			// Cool. Turn off LED.
			HAL_GPIO_WritePin(GPIOC, GPIO_PIN_6, GPIO_PIN_RESET);
		}

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	}
  /* USER CODE END 3 */
}

